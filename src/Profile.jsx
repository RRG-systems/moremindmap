function categoryForBosHeading(heading) {
  const match = BOS_SECTION_MAPPINGS.find((section) => section.pattern.test(heading));
  return match?.title || "";
}

function headingLooksUseful(text) {
  const cleaned = cleanVisibleProfileText(text);
  if (cleaned.length < 3 || cleaned.length > 110) return false;
  if (/^(more mindmap|profile id|prepared for|generated by|back to home)$/i.test(cleaned)) return false;
  return /[a-z]/i.test(cleaned);
}

function parseBosHtmlSections(html) {
  if (typeof window === "undefined" || !window.DOMParser || !html) return [];
  const parsed = new window.DOMParser().parseFromString(String(html), "text/html");
  const body = parsed.body;
  if (!body) return [];

  const headingNodes = Array.from(body.querySelectorAll("h1,h2,h3,h4,strong,b,[class*='title'],[class*='heading']"))
    .filter((node) => headingLooksUseful(node.textContent));
  if (!headingNodes.length) return [];

  const fullText = cleanVisibleProfileText(body.textContent || "");
  const positioned = headingNodes
    .map((node) => {
      const heading = cleanVisibleProfileText(node.textContent || "");
      return { heading, index: fullText.indexOf(heading) };
    })
    .filter((item) => item.index >= 0)
    .sort((a, b) => a.index - b.index);

  return positioned.map((item, index) => {
    const nextIndex = positioned[index + 1]?.index ?? Math.min(fullText.length, item.index + 900);
    const bodyText = fullText.slice(item.index + item.heading.length, nextIndex).trim();
    return {
      heading: item.heading,
      text: bodyText.slice(0, 760)
    };
  }).filter((section) => section.heading && section.text);
}

function sentenceWindowAround(visibleText, pattern, maxLength = 700) {
  const match = visibleText.match(pattern);
  if (!match || match.index === undefined) return "";
  const start = Math.max(0, match.index - 180);
  return visibleText.slice(start, start + maxLength).replace(/\s+/g, " ").trim();
}

function buildFallbackBosSections(visibleText) {
  return BOS_SECTION_MAPPINGS
    .map((section) => {
      const text = sentenceWindowAround(visibleText, section.pattern);
      return text ? { category: section.title, heading: section.title, text } : null;
    })
    .filter(Boolean);
}

function reliableBosSectionText(text) {
  const cleaned = cleanVisibleProfileText(text).slice(0, 1200);
  if (cleaned.length < 90) return "";
  if (/^(not available|none listed)$/i.test(cleaned)) return "";
  return cleaned;
}

function buildBosSectionTranslatorPayloadsFromRenderedProfile(html, profileId) {
  const visibleText = cleanVisibleProfileText(visibleTextFromHtml(html, 14000));
  const mapped = new Map();

  parseBosHtmlSections(html).forEach((section) => {
    const category = categoryForBosHeading(section.heading);
    const text = reliableBosSectionText(section.text);
    if (!category || !text || mapped.has(category)) return;
    mapped.set(category, {
      category,
      heading: section.heading,
      text
    });
  });

  if (mapped.size < 2) {
    buildFallbackBosSections(visibleText).forEach((section) => {
      const text = reliableBosSectionText(section.text);
      if (!text || mapped.has(section.category)) return;
      mapped.set(section.category, {
        category: section.category,
        heading: section.heading,
        text
      });
    });
  }

  const ordered = [
    ...BOS_SECTION_BUTTON_PRIORITY
      .map((category) => mapped.get(category))
      .filter(Boolean),
    ...Array.from(mapped.values()).filter((section) => !BOS_SECTION_BUTTON_PRIORITY.includes(section.category))
  ];

  return ordered
    .slice(0, 6)
    .map((section) => ({
      category: section.category,
      heading: section.heading,
      buttonLabel: BOS_SECTION_BUTTON_LABELS[section.category] || `Explain ${section.category.toLowerCase()}`,
      source: {
        source_type: "bos_section",
        source_title: section.heading || section.category,
        source_excerpt: [
          `${section.category}:`,
          `Source heading: ${section.heading || section.category}`,
          section.text
        ].join("\n"),
        profile_context: `Retrieved Profile ID: ${profileId || "available"}`
      }
    }));
}

function buildBosTranslatorExcerptFromRenderedProfile(html, profileId) {
  const visibleText = cleanVisibleProfileText(visibleTextFromHtml(html, 14000));
  const htmlSections = parseBosHtmlSections(html);
  const mapped = new Map();

  htmlSections.forEach((section) => {
    const category = categoryForBosHeading(section.heading);
    if (!category || mapped.has(category)) return;
    mapped.set(category, {
      heading: section.heading,
      text: cleanVisibleProfileText(section.text).slice(0, 760)
    });
  });

  if (mapped.size < 3) {
    buildFallbackBosSections(visibleText).forEach((section) => {
      if (!mapped.has(section.category)) {
        mapped.set(section.category, {
          heading: section.heading,
          text: section.text.slice(0, 760)
        });
      }
    });
  }

  const structuredSections = Array.from(mapped.entries()).map(([category, section]) => (
    `${category}:\nSource heading: ${section.heading}\n${section.text}`
  ));

  const fallbackLength = structuredSections.length >= 3 ? 900 : 1800;
  const fallback = visibleText.slice(0, fallbackLength);

  return [
    "BOS section-aware excerpt: true",
    "Profile context:",
    `Retrieved Profile ID: ${profileId || "available"}`,
    "",
    ...structuredSections,
    "",
    "Visible source excerpt fallback:",
    fallback
  ]
    .join("\n")
    .replace(/\n{3,}/g, "\n\n")
    .slice(0, 6000);
}

/**
 * buildApiUrl - Safely join API base URL with endpoint path
 * Handles both absolute URLs (production) and relative paths (local dev with proxy)
 * 
 * Examples:
 * - buildApiUrl('https://example.com', '/api/endpoint') → 'https://example.com/api/endpoint'
 * - buildApiUrl('/', '/api/endpoint') → '/api/endpoint'
 * - buildApiUrl('', '/api/endpoint') → '/api/endpoint'
 */
function buildApiUrl(baseUrl, endpoint) {
  // Handle relative/proxy case: / or empty string
  if (baseUrl === '/' || baseUrl === '' || !baseUrl) {
    // For relative paths, just return the endpoint as-is
    return endpoint.startsWith('/') ? endpoint : `/${endpoint}`
  }
  
  // Handle absolute URL case (production)
  // Remove trailing slash from baseUrl, remove leading slash from endpoint, join with /
  const base = baseUrl.endsWith('/') ? baseUrl.slice(0, -1) : baseUrl
  const path = endpoint.startsWith('/') ? endpoint : `/${endpoint}`
  return `${base}${path}`
}

