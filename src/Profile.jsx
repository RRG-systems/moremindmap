function visibleTextFromHtml(html, maxLength = 5000) {
  if (!html) return "";
  if (typeof window !== "undefined" && window.DOMParser) {
    const parsed = new window.DOMParser().parseFromString(String(html), "text/html");
    return String(parsed.body?.textContent || "")
      .replace(/\s+/g, " ")
      .trim()
      .slice(0, maxLength);
  }
  return String(html)
    .replace(/<[^>]+>/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, maxLength);
}

function cleanVisibleProfileText(text) {
  return String(text || "")
    .replace(/\s+/g, " ")
    .replace(/MORE MINDMAP SYSTEM/gi, " ")
    .replace(/Generated by MORE MindMap System/gi, " ")
    .replace(/Prepared for:/gi, " ")
    .trim();
}

